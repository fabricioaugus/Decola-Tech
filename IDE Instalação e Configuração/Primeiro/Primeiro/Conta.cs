using System;
using System.Collections.Generic;
using System.Text;

namespace Primeiro
{
    public class Conta
    {
        public int Soma(int a, int b) {
            return a + b;
        }
        public int Subtrair(int a, int b)
        {
            return a - b;
        }
        public int Dividir(int a, int b)
        {
            return a / b;
        }
        public int Multiplicar(int a, int b)
        {
            return a * b;
        }

    }
}
